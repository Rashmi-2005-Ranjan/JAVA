public class Main {
    public static void main(String[] args) {
        System.out.println ("Hello Kg Coding...");

        //Showing Double Quotes In Output
        System.out.println ("\"Hello Kg Coding...\"");

        //Showing Single Quotes In Output
        System.out.println ("\'Hello Kg Coding...\'");

        //Showing Backslash In Output
        System.out.println ("\\Hello Kg Coding...\\");

        //Showing New Line In Output
        System.out.println ("Hello Kg Coding 1st Line...\nHello Kg Coding 2nd Line...\nHello Kg Coding 3rd Line...");

        //Showing Tab In Output
        System.out.println ("H\tello\tKg\tCoding1stLine...");

        //Showing Backspace In Output
        System.out.println ("Hello Kg Coding 1st Line...\bHello Kg Coding 2nd Line...\bHello Kg Coding 3rd Line...");
    }
}