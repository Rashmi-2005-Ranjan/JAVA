public class ForLoop {
    public static void main(String[] args) {


        //Print Numbers From 1 to 100 using For Loop
        for(int i=1;i<=100;i++){
            System.out.print(i+" , ");
        }
        System.out.println();


        //Print Numbers From 1 to 100 using While Loop
        int j=1;
        while(j<=100){
            System.out.print (j + " , ");
            j++;
        }
        System.out.println();
    }
}
