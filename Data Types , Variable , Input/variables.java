public class variables {
    public static void main(String[] args) {
        // Integer Variables
        int myNumber = 865;

        int yourNumber;

        yourNumber = 457;

        System.out.println("myNumber: " + myNumber);
        System.out.println("yourNumber: " + yourNumber);

        //Boolean Variables
        boolean isVegetarian= false;
        System.out.println("Is Vegetarian: " + isVegetarian);

        float myFloat = 5.0f;
        System.out.println ("myFloat: " + myFloat);

        //Double Variables
        double myDouble = 5.45786;
        System.out.println("myDouble: " + myDouble);

        //String Variables
        String myString = "Good Morning";
        System.out.println("myString: " + myString);

        //Character Variables
        char myCharacter='a';
        System.out.println("myCharacter: " + myCharacter);
    }
}