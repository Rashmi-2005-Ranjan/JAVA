public class UnaryOperator {
    public static void main(String[] args) {
        // Unary Operator
        // Unary operator is an operator that takes only one operand.
        // It can be used to increment or decrement a variable by 1.
        // It can also be used to negate a value.

        int a = 5;
        System.out.println("The value of a is: " + a);
        System.out.println("The value of ++a is: " + ++a); // Pre-increment
        System.out.println("The value of a++ is: " + a++); // Post-increment
        System.out.println("The value of --a is: " + --a); // Pre-decrement
        System.out.println("The value of a-- is: " + a--); // Post-decrement
        System.out.println("The value of a is: " + a);
        System.out.println ("The value of -a is: " + -a); // Negation
    }
}
